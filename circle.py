import math


def area(r):
    '''
    takes the radius of a circle
    returns the area
    example of usage:
    area(10) = 314.1592653589793
    '''
    return math.pi * r * r




def perimeter(r):
    '''
    takes the radius of a circle
    returns the perimeter
    example of usage:
    perimeter(10) = 62.83185307179586
    '''
    return 2 * math.pi * r